    private void populate() {
        List<Korisnik> korisnici = Kontroler.getInstance().vratiKorisnike();
        for (Korisnik k : korisnici) {
            cbKorisnici.addItem(k);
        }

        List<Vozilo> vozila = Kontroler.getInstance().vratiSlobodnaVozila(new Vozilo());
        mtv = new ModelTabeleVozila(vozila);
        tblIznajmi.setModel(mtv);

        List<StavkaRacuna> iznajmljena = Kontroler.getInstance().vratiIznajmljeno(new StavkaRacuna());
        mti = new ModelTabeleStavkaRacuna(iznajmljena);
        tblIznajmljeno.setModel(mti);

        stavke = Kontroler.getInstance().ucitajListuCenovnika(new StavkaCenovnika());
        for (StavkaCenovnika c : stavke) {
            cbKategorije.addItem(c);
        }
        
        tblZaIznajmljivanje.setModel(new ModelTabeleStavkaRacuna(new ArrayList<StavkaRacuna>()));
    }

    private void reset() {
        txtRegistracija.setText("");
        txtIznosPaketa.setText("");
        txtIznosVozila.setText("");
        txtUkupno.setText("");
        dateIzdato.setDate(null);
        dateVracanje.setDate(null);
    }

    private List<StavkaRacuna> listaStavki(Korisnik korisnik) {
        List<StavkaRacuna> lista = ((ModelTabeleStavkaRacuna) tblZaIznajmljivanje.getModel()).getIznajmljeno();

        for (StavkaRacuna sr : lista) {
            sr.setKorisnik(korisnik);
            sr.getVozilo().setStatusVozila(StatusVozila.ZAUZETO);
            System.out.println(sr);
        }

        return lista;
    }

    private int razlikaUDatumima() {
        LocalDate izdato = dateIzdato.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        LocalDate vracanje = dateVracanje.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        
        return vracanje.getDayOfYear() - izdato.getDayOfYear();
    }

