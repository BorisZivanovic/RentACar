    private void tblIznajmiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblIznajmiMouseClicked
        if (tblIznajmi.getSelectedRow() != -1) {
            ukupno -= iznos;

            Vozilo vozilo = ((ModelTabeleVozila) tblIznajmi.getModel()).vratiVozilo(tblIznajmi.getSelectedRow());
            txtRegistracija.setText(vozilo.getRegistarskiBroj());

            iznos = vozilo.getKategorijaVozila().getCena();
            txtIznosVozila.setText("" + iznos);

            ukupno += iznos;
            txtUkupno.setText("" + ukupno);
        }
    }//GEN-LAST:event_tblIznajmiMouseClicked

    private void btnIznajmiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIznajmiActionPerformed
        if (((ModelTabeleStavkaRacuna) tblZaIznajmljivanje.getModel()).getIznajmljeno().size() == 0)
            JOptionPane.showMessageDialog(this, "Molimo izaberite vozilo za izdavanje");
        else {
            Korisnik korisnik = (Korisnik) cbKorisnici.getSelectedItem();
            List<StavkaRacuna> lista = listaStavki(korisnik);
            Racun r = new Racun(Date.valueOf(LocalDate.now()),
                    0.85 * total, 0.15 * total, total,
                    korisnik,
                    lista);
            Odgovor o = Kontroler.getInstance().iznajmiVozilo(r);
            if(o.isUspesno()) {
                tblZaIznajmljivanje.setModel(new ModelTabeleStavkaRacuna(new ArrayList<StavkaRacuna>()));
                lista = ((Racun) o.getOdgovor()).getStavkaRacuna();
                for(StavkaRacuna sr : lista) {
                    mti.dodajIznajmljeno(sr);
                    Kontroler.getInstance().getGf().azuriraj(sr.getVozilo());
                }
            }
        }
    }//GEN-LAST:event_btnIznajmiActionPerformed

    private void cbKategorijeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbKategorijeItemStateChanged
        ukupno -= paket;

        paket = ((StavkaCenovnika) cbKategorije.getSelectedItem()).getCena();
        txtIznosPaketa.setText("" + paket);

        ukupno += paket;
        txtUkupno.setText("" + ukupno);
    }//GEN-LAST:event_cbKategorijeItemStateChanged

    private void btnIzbaciActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbaciActionPerformed
        if (tblZaIznajmljivanje.getSelectedRow() != -1) {
            StavkaRacuna izbaci = ((ModelTabeleStavkaRacuna) tblZaIznajmljivanje.getModel()).vrati(tblZaIznajmljivanje.getSelectedRow());
            double cena = izbaci.getIznosSaPDV();
            mtv.dodajVozilo(izbaci.getVozilo());

            ((ModelTabeleStavkaRacuna) tblZaIznajmljivanje.getModel()).izbaci(tblZaIznajmljivanje.getSelectedRow());
            total -= cena;
            txtRacun.setText("" + total);
        } else
            JOptionPane.showMessageDialog(this, "Niste izabrali");
    }//GEN-LAST:event_btnIzbaciActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        if (!txtRegistracija.getText().equals("") && dateIzdato.getDate() != null && dateVracanje.getDate() != null
                && dateIzdato.getDate().before(dateVracanje.getDate())) {
            Vozilo v = mtv.vratiVozilo(tblIznajmi.getSelectedRow());
            Korisnik korisnik = (Korisnik) cbKorisnici.getSelectedItem();

            int razlika = razlikaUDatumima();
            ukupno *= razlika;
            
            StavkaRacuna novo = new StavkaRacuna(0.85 * ukupno, 0.15 * ukupno, ukupno,
                    korisnik, v,
                    new Iznajmljivanje(korisnik, v, dateIzdato.getDate(), dateVracanje.getDate()));

            ((ModelTabeleStavkaRacuna) tblZaIznajmljivanje.getModel()).dodajIznajmljeno(novo);
            mtv.izbrisiVozilo(tblIznajmi.getSelectedRow());

            total += ukupno;
            txtRacun.setText("" + total);
            reset();
        } else
            JOptionPane.showMessageDialog(this, "Molimo unesite ispravne podatke");
    }//GEN-LAST:event_btnDodajActionPerformed

