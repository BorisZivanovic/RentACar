    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if (!proveraValidacije()) {
            Korisnik noviKorisnik = new Korisnik(Long.parseLong(txtID.getText()),
                    txtIme.getText(), txtPrezime.getText(),
                    Long.parseLong(txtJMBG.getText()),
                    dateChooser.getDate(),
                    (Mesto) cbMesta.getSelectedItem());
            Odgovor o = Kontroler.getInstance().izmeniKorisnika(noviKorisnik);
            if (o.isUspesno()) {
                mtk.izmeniPodatke(noviKorisnik);
                resetujPolja();
            } else {
                JOptionPane.showMessageDialog(this, o.getPoruka());
            }
        } else
            JOptionPane.showMessageDialog(this, "Molimo unesite neophodne podatke");
    }//GEN-LAST:event_btnIzmeniActionPerformed

