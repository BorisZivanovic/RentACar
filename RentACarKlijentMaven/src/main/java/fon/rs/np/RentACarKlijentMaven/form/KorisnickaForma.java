    private void populate() {
        List<Mesto> mesta = Kontroler.getInstance().vratiMesta();
        for (Mesto mesto : mesta) {
            cbMesta.addItem(mesto);
        }

        List<Korisnik> korisnici = Kontroler.getInstance().vratiKorisnike();
        mtk = new ModelTabeleKorisnici(korisnici);
        tblKorisnici.setModel(mtk);

        try {  //01/01/2023
            uslovniDatum = sdf.parse(LocalDate.now().getDayOfMonth() + "/"
                    + LocalDate.now().getMonthValue() + "/" + (LocalDate.now().getYear() - 18));
            //System.out.println(uslovniDatum);
        } catch (ParseException ex) {
            Logger.getLogger(KorisnickaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private boolean proveraValidacije() {
        return txtIme.getText().equals("")
                || txtJMBG.getText().equals("") || txtPrezime.getText().equals("")
                || dateChooser.getDate().after(uslovniDatum);
    }

    private void resetujPolja() {
        txtID.setText("");
        txtIme.setText("");
        txtPrezime.setText("");
        txtJMBG.setText("");
        dateChooser.setDate(null);
    }

